def get_suggestion(cuisine, main_ingredient=None, cooking_method=None):
    suggestions = []
    if cuisine.lower() == "indian" and main_ingredient:
        for recipe, details in indian_vegetarian_recipes.items():
            if main_ingredient.lower() in [ing.lower() for ing in details["ingredients"]]:
                suggestions.append(f"Consider making {recipe}.")
    elif cuisine.lower() == "italian" and main_ingredient:
        for recipe, details in italian_vegetarian_recipes.items():
            if main_ingredient.lower() in [ing.lower() for ing in details["ingredients"]]:
                suggestions.append(f"Consider making {recipe}.")
    elif cuisine.lower() == "indian":
        suggestions.append("Here are some vegetarian Indian dishes: " + ", ".join(indian_vegetarian_recipes.keys()))
    elif cuisine.lower() == "italian":
        suggestions.append("Here are some vegetarian Italian dishes: " + ", ".join(italian_vegetarian_recipes.keys()))
    else:
        suggestions.append("Sorry, I can only suggest vegetarian Indian and Italian dishes right now.")

    return suggestions

def get_recipe_details(recipe_name, cuisine):
    if cuisine.lower() == "indian" and recipe_name in indian_vegetarian_recipes:
        return indian_vegetarian_recipes[recipe_name]
    elif cuisine.lower() == "italian" and recipe_name in italian_vegetarian_recipes:
        return italian_vegetarian_recipes[recipe_name]
    else:
        return None

